public class One {
    public static void main(String[] args) {
        // Types of interfaces in Java
        // 3. Marker Interface
        // 2. Functional Interface / SAM
        // 1. Normal Interface

        /*
         * In Interface we can only have public abstract methods
         * We can only declare them
         */

        /*
         * 1. Normal Interface
         * Interface which has two or more methods thats is called normal interface
         */

        /*
         * 2. Functional Interface / SAM
         * Interface which has only one abstract method is called functional interface
         * or SAM (Single Abstract Method)
         */

        /*
         * 3. Marker Interface
         * Interface which has no methods is called marker interface
         */

        /*
         * Serialization and Deserialization
         * Definition: The process of converting an object into a byte stream
         * (serialization) and
         * reconstructing the object from the byte stream (deserialization) is called
         * serialization and deserialization.
         */
    }
}